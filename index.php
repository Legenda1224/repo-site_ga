        <h3>
            Функции программиста
        </h3>
        <p>
            Разрабатывать новые операционные системы, 
            приложения и программы наподобие MS Office или 
            Adobe Photoshop, работая в России, программисту вряд 
            ли придется. Этим занимаются профессионалы высшей квалификации 
            таких иностранных фирм-производителей, как Microsoft, Sun, Oracle, 
            Adobe и других. В отечественных компаниях подобные проекты – редкость, 
            и пользователи, как правило, устанавливают на свои ПК в основном западные 
            продукты. Отдельные российские фирмы-разработчики программного обеспечения 
            существуют, но их немного, например «Лаборатория Касперского», 
            которая выпускает антивирусные программы. Созданием программ занимаются 
            и государственные структуры: НИИ и конструкторские бюро 
            военно-промышленного комплекса, космонавтики, но их крайне мало.
        </p>
        <p>
            В основном в России требуются программисты-
            "прикладники«: практически любая компания сегодня 
            нуждается в автоматизации своего бизнеса, в возможности 
            работать по внутренней компьютерной сети и быстро обмениваться 
            информацией в электронном виде между подразделениями и отделами, 
            вести учет продукции, закупок, реализованного товара.
            Они адаптируют, а если нужно, и разрабатывают пакет 
            специальных программ, учитывая особенности бизнеса организации: 
            одно дело автоматизировать деятельность издательского дома, другое 
            – торговой фирмы или ресторана. Однако многим предприятиям требуется 
            не только автоматизация бизнеса, но и постоянный мониторинг налаженной
            системы, ее изменение в случае расширения компании, поэтому
            работодатели готовы к зачислению программистов к себе в штат. 
            Все же некоторые организации предпочитают заказывать автоматизацию
            своего бизнеса специализированным ИТ-компаниям, предоставляющим такие услуги.
            Способность к самообучению – один из главных навыков, которым должен обладать программист.
        </p>
