		<h3>Что нужно знать Web-разработчику</h3>
			<p class="h2_text">Веб разработчику нужно знать, что современный сайт – это прежде всего средство достижения определенного результата, и для всех успешных сайтов – этот результат коммерческий. Клиент приходит к разработчику не за сайтом, или веб приложением, как за таковым, а за тем, чтобы решить свои проблемы в информационном поле.</p>
		
		<h3>Обязанности на работе</h3>
			<p class="h2_text">Веб-программист работает с софтом, соблюдая примерно следующий алгоритм деятельности:</p>
		<ul class="h2_text">
			<li>олучение от заказчика задачи, прояснение ее целей;</li>
			<li>совместная с дизайнерами, менеджерами, маркетологами проработка деталей;</li>
			<li>написание необходимых программ, которые обеспечат функционирование ресурса;</li>
			<li>составление технической документации;</li>
			<li>тестирование сайта, выявление и устранение недоработок и багов;</li>
			<li>запуск проекта и его презентация клиенту;</li>
			<li>дальнейшее сопровождение и обновление ресурса (по договоренности с заказчиком)</li>
		</ul>
		
			<p class="h2_text">Для качественного выполнения поставленной задачи хороший web-разработчик должен:</p>
		<ul class="h2_text">
			<li>работать с одним, а лучше с несколькими языками программирования (PHP, JavaScript, Ruby, Perl, C++), особенно важно это для создателей бек-энда;</li>
			<li>уметь использовать крупные базы данных;</li>
			<li>знать HTML, CSS, jQuery, различные фреймворки (например, Bootstrap, Yii или Foundation);</li>
			<li>хорошо владеть техническим английским языком, а для работы с иностранными клиентами, и разговорным.</li>
			<li>составление технической документации;</li>	
		</ul>
		
		<H3>Как построить карьеру</h3>
			<p class="h2_text">Веб-разработку многие относят к профессии мечты: 
			человек занимается любимым делом и получает за это 
			неплохие деньги. Чем выше уровень специалиста, тем более 
