        <h3>
            История професссии - программист
        </h3>
        <p>
            <strong>
                 Программист
            </strong>
             — это человек, который разрабатывает компьютерные программы 
            и создает их на основе кодов, специальных математических моделей. 
            Программист — специалист, занимающийся непосредственной разработкой 
            программного обеспечения для различного рода вычислительно-операционных систем.
        </p>
        <p>
             Часто первым программируемым устройством принято 
             считать жаккардовый ткацкий станок, построенный в 1804
             году Жозефом Мари Жаккаром, который произвёл революцию в 
             ткацкой промышленности, предоставив возможность программировать 
             узоры на тканях при помощи перфокарт. Первое программируемое вычислительное 
             устройство, Аналитическую машину, разработал Чарлз Бэббидж (но не смог её построить). 
             Первый работающий программируемый компьютер (1941 год), первые программы для него,
             а также (с определёнными оговорками) первый язык программирования высокого уровня
             Планкалкюль создал немецкий инженер Конрад Цузе. Имена людей, впервые начавших 
             профессионально выполнять работу собственно по программированию 
             (в отрыве от наладки аппаратуры компьютера), история не сохранила, 
            так как поначалу программирование рассматривалось как второстепенная наладочная операция.
            В настоящее время ведутся множественные разработки в области вычислительной 
            техники и программирования, и уже достигнуты невероятные успехи. 
            О будущем компьютеризации ведутся оживленные споры среди ученых, но, 
            несомненно, результаты прогресса в этой области превзойдут все наши ожидания.
