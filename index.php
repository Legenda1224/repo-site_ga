        <p>
            - Системные программисты разрабатывают операционные системы,
            интерфейсы к распределенным базам данных, работают с сетями. 
            Специалисты этой категории являются самыми редкими и высокооплачиваемыми.
        </p>
        <p>
            - Web-программисты имеют дело с сетями, но, как правило, 
            с глобальными, такими, как Internet. 
            Они пишут web-интерфейсы к базам данных, создают динамические web-страницы и т.п.
        </p>
