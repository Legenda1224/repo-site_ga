	<h2>Кто-такой Web-разработчик</h2>
		<p class="h2_text1">Веб-разработчик — это специалист занимающийся написанием, обновлением, исправлением и совершенствованием алгоритмов для приложений, сайтов и отдельных элементов, с использованием разных языков программирования. Результат работы веб-разработчика — это интуитивно понятные и дружелюбные для пользователя цифровые продукты, которые мы используем в повседневной жизни.</p>
	<img id="img1" src="./img/home_3_about_page_intro_video_bg.jpg">
		<p class="h2_text">Слово «веб-разработчик» кажется вполне понятным — очевидно же, что он разрабатывает что-то для веба. На деле всё не так просто. Современный интернет мало похож на веб девяностых или нулевых: технологий столько, что уследить за всем сразу просто нереально. Поэтому в профессии веб-разработчика есть три больших специализации:</p>
		
		
	<h3>История профессии</h3>	
		<p  class="h2_text">Первые шаги в построении глобальной компьютерной сети были сделаны в 60-х годах XX века,
		а уже в 1969 году состоялся «премьерный» сеанс связи между двумя 
		удаленными ее абонентами. В последующие десятилетия Интернетом пользовалось 
		небольшое количество узких специалистов. Ситуация кардинально изменилась в начале
		90-х, когда в обиход вошли персональные компьютеры, а всеобщий доступ во Всемирную
		паутину обеспечили браузеры с хорошим интерфейсом (например, NCSA Mosaic). С тех пор 
		прошла только четверть века, а Интернетом пользуется уже больше половины населения Земли.
		Всемирная сеть проникла во все области человеческой деятельности, без нее не обойтись в 
		финансовой сфере, сервисе, коммуникациях и т. д. Массово стали возникать специальности, 
		связанные с информационными технологиями, одна из наиболее дефицитных среди них – программист сетевых ресурсов.</p>
		
		<p id="arial">Frontend-разработчик </p><p class="h2_text"> Программист, который создаёт интерфейс, видимую часть приложения. Она, как кассир в McDonald’s, берёт на себя общение с клиентом, принимает заказы. Когда мы заходим на сайты, мы обычно нажимаем на кнопки или отправляем товар в корзину. За всё это отвечает frontend. Нас не интересует, что там внутри, — главное, чтобы всё хорошо работало. Ну а если мобильная версия сайта по бокам вылезает за экран — виноват frontend-разработчик.</p>
		<p id="arial">Backend-разработчик </p><p class="h2_text"> Отвечает за внутренности, серверную часть сайта. Как повар в McDonald’s, он принимает заказы от кассира, готовит и передаёт обратно на кассу. Если вы поставили галочку напротив «китайские чайники до 2 000 рублей», а сайт показал гаечные ключи — передайте привет backend-разработчику.</p>
		<p id="arial">Fullstack-разработчик </p><p class="h2_text"> Многостаночник, который умеет и backend, и frontend. Как хозяин небольшого кафе, который принимает заказы, готовит и разносит еду. Делает он всё немного медленнее, а навыки до идеала прокачать сложно. Зато в курсе всех процессов — от и до. Опытные разработчики говорят, что в fullstack проще прийти из backend.</p><br>
		<p class="h2_text">Веб-разработка — самое популярное направление в программировании. Согласно исследованию Stack Overflow за 2020 год, большинство программистов считают себя frontend-, backend- или fullstack-разработчиками. Но лидируют в рейтинге те, кто занимается бэкендом.</p>
	<img  src="./img/234234.png">
		<p class="h2_text">Это подтверждает и опрос о самых популярных технологиях среди программистов: так или иначе связанные с вебом лидируют с большим отрывом. А на первом месте здесь JavaScript — его использует почти 70% веб-разработчиков в мире.</p>
