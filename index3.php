            хорошем уровне. Эта программа удивляет своими возможностями,
            освоение которых потребует времени и сил. Экспериментируйте 
            с цветами, кистями, эффектами. Не обязательно сразу рисовать 
            сайт, важно выработать чувство вкуса и технические навыки при 
            использовании фотошопа.
