              Web-дизайнер
        </h3>
        <p>
            <strong>Web-дизайнер –</strong> это смесь знаний новейших 
            технологий и умения заинтересовать публику у экранов монитора.
            Для лучшего понимания обозначим, что это ещё одна разновидность
            дизайна, где главной задачей является создание удобного интерфейса 
            для сайта или веб-приложения. Такой человек на «ты» с различными программами, а
            также креативен и не лишён художественного вкуса.
            Сфера влияния веб-дизайнера – это создание и оформление интернет-проектов. 
            Работа считается отлично выполненной, если проект набирает стремительную популярность,
            а также отличается особым удобством при использовании.
        </p>
        <h3>
            Чем занимается веб-дизайнер
        </h3>
        <p>
            Эта профессия требует хороших усилий, здесь важно упорство. 
            Чтобы овладеть этой специальностью, нужно стремиться постоянно 
            учиться, практиковаться, повышать свои навыки.
        </p>
        <p>
             Любой web-дизайнер должен:
        </p>
        <ul>
            <li>
                уметь работать с различными графическими редакторами;
            </li>
            <li>
                понимать, каким должен быть внешний вид сайтов или блогов;
            </li>
            <li>
                развивать в себе такие способности как: креативность, творческое мышление.
            </li>
            <li>
                немного разбираться в HTML и CSS.
            </li>
        </ul>
        <p>
            В основном веб-дизайнеры создают изображения для блогов, 
            сайтов, страничек в соц. сетях. Нарисованные шапки на
            известных блогах, пабликах соц. сетей, оформление 
            аккаунтов любого интернет-сервиса это дело рук web-дизайнера.
        </p>

        <p>
            Веб-дизайнер должен понимать, что делает верстальщик 
            – человек, который пишет код под нарисованный шаблон.
            Но верстать интернет-страницы ему не обязательно. Его
            основная задача – создавать дизайн, оформлять Интернет-ресурсы.
        </p>
        <h3>
             Должен ли веб-дизайнер уметь рисовать
        </h3>
        <p>
            Есть люди, которые имеют хороший вкус и чувство стиля. Но, 
            они вряд ли нарисуют красивый рисунок рукой. При этом, они 
            умеют создавать классные дизайны, работая в графических редакторах.
            Поэтому, умея работать с графическими редакторами, легко можно делать
            уникальные и привлекательные дизайны для сайтов и блогов, даже не умея рисовать.
        </p>
