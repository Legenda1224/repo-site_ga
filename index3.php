            себе воспитать. Ходите в кино, фотографируйте, обращайте внимание 
            на рекламу, посещайте выставки, интересуйтесь новинками живописи, 
            покупайте книжки, уделяйте время путешествиям в красивые места. 
            Делайте всё то, что вас вдохновляет на творчество!
        </p>
        <p>
