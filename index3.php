        <p>
            Самое главное, что должен уметь специалист в области дизайна
             – это создавать удобный и понятный дизайн, который надолго 
             запомнится пользователям. Если картинка сайта, например, 
             красивая, но сама навигация сложная для пользователей, то 
             это вызовет недовольство, а поэтому сам дизайн уже будет 
             восприниматься по-другому. По этой причине в первую очередь 
             нужно думать об удобстве пользователя и о том, как доставить ему радость.
        </p>
