            <li>наличие отличного художественного вкуса и стиля;</li>
            <li>знание основ юзабилити;</li>
            <li>знание принципов вёрстки и языков HTML, CSS;</li>
            <li>иметь хорошее представление об интернет-маркетинге;</li>
            <li>возможность быть всегда онлайн (если речь идёт о работе на дому).</li>
        </ul>
        <p>
            Главные <strong>качества:</strong>
        </p>
        <ul>
            <li>усидчивость;</li>
            <li>большой запас креативности, идей и умение их воплощать в реальность;</li>
            <li>коммуникативность, умение общаться с заказчиками;</li>
            <li>хорошая память и образное мышление;</li>
            <li>терпеливость, особенно с заказчиками.</li>
        </ul>
        <h3>
             Кому подходит веб дизайн
        </h3>
        <p>
            Ошибочное мнение, что сфера веб дизайна ориентирована 
            исключительно на творческих людей. Мол если вы технарь,
            то в веб дизайн вам лучше не соваться. На самом деле, как
            я уже сказал выше, web design уже давно ушел от творчества 
            в сторону функциональности.
        </p>
        <p>
            Поэтому вам не обязательно быть творческой личностью. 
            Достаточно иметь желание создавать крутые проекты, которыми
            будут пользоваться другие люди. Если вам нравится ощущение того,
            что в какой-то степени вы будете делать виртуальный мир чуточку 
            удобней и привлекательней (путем создания дизайна), тогда это профессия вам подойдет.
        </p>
        <p>
            Еще одним плюсом будет перфекционизм в разумных пределах.
            Внимание к мелочам — это важный аспект любого профессионального
            веб дизайнера, потому что именно из мельчайших деталей складывается общая картина.
        </p>
        <p>
            Помимо этого, аналитический склад ума также добавит вам
            несколько баллов в копилку. Как я уже говорил, создание 
            дизайна базируется в первую очередь на проектировании и 
            определенных исследованиях.
        </p>
        <p>
            Нужно ли высшее профильное образование? Нет. 
            В этой сфере вам достаточно пройти курсы, и вы
            уже сможете претендовать на вакансии. Более того, 
            профильных высших заведений с этим направлением очень 
            мало, и они есть не в каждом городе.
        </p>
        <p>
            По большому счету, веб дизайном может заняться абсолютно любой человек, 
