            Многие не дойдут и до середины пособия – надоест. 
            Самый простой способ освоить фотошоп, не умея рисовать, 
            пойти учиться на курсы дизайнеров.
        </p>
        <p>
            Там будут учить люди, которые уже работают в программе, 
            и они даже могут поделиться с Вами своими «фишками», а этого 
            в книгах нет. Еще один плюс – после курсов на руках будет документ 
            о том, что Вы учились, а работодатели это ценят.
